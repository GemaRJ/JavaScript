/* 
 RESUMEN DE MÉTODOS Y FUNCIONES DE BÚSQUEDA / VALIDACIÓN

 isNaN()
Comprueba si un valor NO es un número (NaN = Not a Number). 
Sirve para validar datos antes de operar con ellos.

 some()
Verifica si AL MENOS un elemento del array cumple una condición. 
Devuelve true o false.

 find()
Devuelve el PRIMER elemento del array que cumple una condición. 
Si no hay coincidencia, devuelve undefined.

 filter()
Devuelve TODOS los elementos que cumplan una condición en un nuevo array. 
Sirve para obtener varias coincidencias.

 includes()
Comprueba si un array o cadena contiene un valor exacto. 
Devuelve true o false.

 indexOf()
Devuelve la posición (índice) de un elemento dentro del array o cadena. 
Si no lo encuentra, devuelve -1.

 Diferencias básicas:
- isNaN → valida números.
- some → comprueba si existe alguna coincidencia.
- find → devuelve una coincidencia.
- filter → devuelve varias coincidencias.
- includes → confirma si un valor existe.
- indexOf → indica la posición de un valor.
*/
